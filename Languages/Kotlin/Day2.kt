// Write a program that declares an integer, a string, and a floating-point number, then prints these values.

fun main() {
    val myInt: Int = 10
    val myString: String = "Hello, Kotlin!"
    val myFloat: Float = 3.14f

    println("Integer value: $myInt")
    println("String value: $myString")
    println("Floating-point value: $myFloat")
}
