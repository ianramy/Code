// Write a program that declares an integer, a string, and a floating-point number, then prints these values.

#include <iostream>
#include <string>
using namespace std;

int main() {
    int integer = 5;
    string str = "Hello, World!";
    float floating = 3.14;

    cout << "Integer: " << integer << endl;
    cout << "String: " << str << endl;
    cout << "Floating-point: " << floating << endl;

    return 0;
}

// Output:
// Integer: 5
// String: Hello, World!
// Floating-point: 3.14
