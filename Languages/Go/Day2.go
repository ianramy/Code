// Write a program that declares an integer, a string, and a floating-point number, then prints these values.

package main
import "fmt"

// Declare an integer, a string, and a floating-point number.
func main() {
    integer := 42
    str := "Hello, World!"
    float := 3.14

	// Print the values.
    fmt.Println("Integer:", integer)
    fmt.Println("String:", str)
    fmt.Println("Float:", float)
}

// Output
// Integer: 42
// String: Hello, World!
// Float: 3.14
