// Write a program that declares an integer, a string, and a floating-point number, then prints these values.

// Declare an integer, a string, and a floating-point number.
let integer = 10;
let string = "Hello, World!";
let floatingPoint = 3.14;

// Print the values of the integer, string, and floating-point number.
console.log(integer);
console.log(string);
console.log(floatingPoint);

// Output:
// 10
// Hello, World!
// 3.14
