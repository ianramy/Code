//  Write a program that prints "Hello, World!" to the console.
public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}

// Java is a statically-typed language, which means that all variables must be declared before they can be used.
// It is also a strongly-typed language, which means that variables must be used in a way that is consistent with their type.
// Java is an object-oriented language, which means that it uses objects to represent data and methods to manipulate that data.
// The public keyword is an access modifier that allows other classes to access this class.
// The static keyword indicates that the main method is a class method, which means that it can be called without creating an instance of the class.
// The void keyword indicates that the main method does not return a value.
// The String[] args parameter is an array of strings that can be passed to the main method when it is called.
// The System.out.println() method is used to print a message to the console.
