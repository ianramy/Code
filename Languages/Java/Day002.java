// Write a program that declares an integer, a string, and a floating-point number, then prints these values.

// Declare an integer, a string, and a floating-point number.
public class Day2 {
    public static void main(String[] args) {
        int integer = 5;
        String string = "Hello, World!";
        float floatingPoint = 3.14f;
        
        // Print the values of the integer, string, and floating-point number.
        System.out.println("Integer: " + integer);
        System.out.println("String: " + string);
        System.out.println("Floating-point: " + floatingPoint);
    }
}

// Output:
// Integer: 5
// String: Hello, World!
// Floating-point: 3.14
