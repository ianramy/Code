// Write a program that declares an integer, a string, and a floating-point number, then prints these values.

using System;

namespace Day2
{
    class Program
    {
        static void Main(string[] args)
        {
            int integer = 5;
            string str = "Hello, World!";
            float floatingPoint = 3.14f;

            Console.WriteLine("Integer: " + integer);
            Console.WriteLine("String: " + str);
            Console.WriteLine("Floating Point: " + floatingPoint);
        }
    }
}

// Output
// Integer: 5
// String: Hello, World!
// Floating Point: 3.14
